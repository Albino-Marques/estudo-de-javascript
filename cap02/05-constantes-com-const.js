//Constantes
// const para declarar constantes
// Importante, sempre declarar constantes com nomes significativos.
// Não se pode começar o nome de uma constante com número.
// Nome de constantes não pode conter espaços ou traços.
/* Usar a formatação de constante camelCase, por exemplo: */
const nomeCliente = "Albino";

// O JavaScript é Case-sensitive nas constantes.
// Declarar é diferente de atribuir!!!!
// Não pode mudar o valor de uma constante.
// UTILIZE CONST.

console.log(nomeCliente);

const nomeCompleto = nomeCliente + " Marques";

console.log(nomeCompleto);

const primeiroNum = 5;
const segundoNum = 12;
const soma = primeiroNum * segundoNum;

console.log(soma);

// String = "Sequência de caracteres como está frase. Sempre entre aspas." | Number = "Numerais."

// Para descobrir o tipo de uma variável/constante = typeof

console.log(typeof soma);

// O mais "+" serve tanto para concatenar strings, e strings com número, quanto somar numeros.

// A soma de uma string com um número sempre resultará em uma string!!!

// CONSTANTES NÃO SÃO ALTERÁVEIS AO LONGO DO CÓDIGO.
