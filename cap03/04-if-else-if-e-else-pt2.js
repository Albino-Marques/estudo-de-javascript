const numero = 15;

if (numero >= 0 && numero <= 5) {
  console.log("O número está entre zero e 5.");
} else if (numero >= 6 && numero <= 8) {
  console.log("O número está entre 6 e 8.");
} else if (numero >= 9 && numero <= 11) {
  console.log("O númeor está entre 9 e 11.");
} else {
  console.log("O número não etá entre 0 e 11.");
}

// O else e o else if dependem do if, sendo que a coodependência deles é de suma importância.